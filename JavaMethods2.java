
public class JavaMethods2 {
	
	// These methods will not be run in this class - they will be used in another class
	
	public int addNums(int x, int y) {
		return x + y;
	}
	
	public int subtractNums(int x, int y) {
		return x - y;
	}
	
	public int multiplyNums(int x, int y) {
		return x * y;
	}
	
	public int divideNums(int x, int y) {
		return x / y;
	}
	
	// Static method
	public static int squareNum(int x) {
		return x * x;
	}
}
